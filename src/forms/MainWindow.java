    // used in editing table rows. Gets the index value for combo box.
    private int getComboMeasurementIndex(String measurementType) {
        int comboIndexValue;
        switch (measurementType) {
            case "grams": 
                comboIndexValue = 0;
                break;
            case "liquid oz": 
                comboIndexValue = 1;
                break;
            case "dry oz":
                comboIndexValue = 2;
                break;    
            case "teaspoon":
                comboIndexValue = 3;
                break;
            case "tablespoon":
                comboIndexValue = 4;
                break;
            case "milligram":
                comboIndexValue = 5;
                break;
            case "liquid cup":
                comboIndexValue = 6;
                break;
            case "dry cup":
                comboIndexValue = 7;
                break;
            case "liquid pint":
                comboIndexValue = 8;
                break;
            case "dry pint":
                comboIndexValue = 9;
                break;
            default:
                comboIndexValue = 0;
                break;
        } 
        return comboIndexValue;
    } 
    
